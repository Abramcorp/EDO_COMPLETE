"""Эталонные позиции чёрных fiducial-меток на страницах декларации УСН.

Получены прямой детекцией на эталонном PDF (Романов УСН 2025), отрендеренном
в 150 DPI. Координаты — пиксели от верхнего-левого угла страницы.

A4 при 150 DPI = 1240 × 1754 px.

Структура: ``{page_num: {mark_tag: (x, y)}}``, где ``mark_tag`` ∈ ``TL/TR/BL/BR``.

ВАЖНО:
    - Стр.1 (Титульный) имеет 3 метки (TL, BL, BR). TL смещена ниже из-за
      штрих-кода и шапки формы. BL/BR — на 1499 (под подписью).
    - Стр.2-4 (Р.1.1, Р.2.1.1, Р.2.1.1 продол.) имеют 2 метки (TL, BR).
      Y-позиция BR = 1575-1584 (зависит от плотности контента).
    - Страницы 5-6 (квитанции от docx-шаблона) меток НЕ имеют — нормализация
      для них не применяется.

Если при будущих изменениях эталонного формата ФНС цифры сдвинутся — обновлять
таблицу здесь и тесты в ``tests/test_page_normalizer.py``.
"""
